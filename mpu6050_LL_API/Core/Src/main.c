/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  LL_APB2_GRP1_EnableClock(LL_APB2_GRP1_PERIPH_AFIO);
  LL_APB1_GRP1_EnableClock(LL_APB1_GRP1_PERIPH_PWR);

  /* System interrupt init*/
  NVIC_SetPriorityGrouping(NVIC_PRIORITYGROUP_4);

  /** NOJTAG: JTAG-DP Disabled and SW-DP Enabled
  */
  LL_GPIO_AF_Remap_SWJ_NOJTAG();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_I2C1_Init();


  /* USER CODE BEGIN 2 */

  uint8_t rxtest[1]={0};

  // Check device ID
  //
  I2C_SendRequest( MPU6050_ADDR, WHO_AM_I_REG, rxtest, 1 );
  LL_mDelay(100);


  //if ( rxtest[0] == 104 ) {

	  // Wake up the sensor
	  //
	  I2C_WriteRequest( MPU6050_ADDR, PWR_MGMT_1_REG, 0x00 );
	  LL_mDelay(20);

	  // Set DATA RATE of 1KHz by writing SMPLRT_DIV register
	  //
	  I2C_WriteRequest( MPU6050_ADDR, SMPLRT_DIV_REG, 0x07 );
	  LL_mDelay(20);

	  // Set accelerometer configuration in ACCEL_CONFIG Register
	  // XA_ST=0,YA_ST=0,ZA_ST=0, FS_SEL=0 -> ± 2g
	  //
	  I2C_WriteRequest( MPU6050_ADDR, ACCEL_CONFIG_REG, 0x00 );
	  LL_mDelay(20);

	  // Set Gyroscopic configuration in GYRO_CONFIG Register
	  // XG_ST=0,YG_ST=0,ZG_ST=0, FS_SEL=0 -> ± 250 °/s
	  //
	  I2C_WriteRequest( MPU6050_ADDR, GYRO_CONFIG_REG, 0x00 );
	  LL_mDelay(20);

  //}


  /* USER CODE END 2 */

  /* Infinite loop */


  /* USER CODE BEGIN WHILE */

  __NOP();
  uint8_t Rec_Data[6];

  while (1)
  {

	// Read accelerations --- //

	I2C_SendRequest( MPU6050_ADDR, ACCEL_XOUT_H_REG, Rec_Data, 6 );

	Accel_X_RAW = (int16_t)(Rec_Data[0] << 8 | Rec_Data [1]);
	Accel_Y_RAW = (int16_t)(Rec_Data[2] << 8 | Rec_Data [3]);
	Accel_Z_RAW = (int16_t)(Rec_Data[4] << 8 | Rec_Data [5]);

	__NOP();

	// Convert the RAW values into acceleration in 'g'
	// we have to divide according to the Full scale value set in FS_SEL
	// I have configured FS_SEL = 0. So I am dividing by 16384.0
	// for more details check ACCEL_CONFIG Register

	Ax = Accel_X_RAW/16384.0;
	Ay = Accel_Y_RAW/16384.0;
	Az = Accel_Z_RAW/16384.0;


	LL_mDelay(200);
	// End read accelerations --- //


    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

/**
  * @brief System Clock Configuration
  * @retval None
  */
void SystemClock_Config(void)
{
  LL_FLASH_SetLatency(LL_FLASH_LATENCY_2);
  while(LL_FLASH_GetLatency()!= LL_FLASH_LATENCY_2)
  {
  }
  LL_RCC_HSE_Enable();

   /* Wait till HSE is ready */
  while(LL_RCC_HSE_IsReady() != 1)
  {

  }
  LL_RCC_PLL_ConfigDomain_SYS(LL_RCC_PLLSOURCE_HSE_DIV_1, LL_RCC_PLL_MUL_9);
  LL_RCC_PLL_Enable();

   /* Wait till PLL is ready */
  while(LL_RCC_PLL_IsReady() != 1)
  {

  }
  LL_RCC_SetAHBPrescaler(LL_RCC_SYSCLK_DIV_1);
  LL_RCC_SetAPB1Prescaler(LL_RCC_APB1_DIV_2);
  LL_RCC_SetAPB2Prescaler(LL_RCC_APB2_DIV_1);
  LL_RCC_SetSysClkSource(LL_RCC_SYS_CLKSOURCE_PLL);

   /* Wait till System clock is ready */
  while(LL_RCC_GetSysClkSource() != LL_RCC_SYS_CLKSOURCE_STATUS_PLL)
  {

  }
  LL_Init1msTick(72000000);
  LL_SetSystemCoreClock(72000000);
}

